const month = 3;

switch (month) {
  case 1:
    console.log("January");
    break;

  case 2:
    console.log("February");
    break;

  case 3:
    console.log("March");
    break;

  case 4:
    console.log("April");
    break;

  default:
    break;
}

//  if break is not used then all the code after when condition meets execute except the default one

//  switch with string

const mon = "march";

switch (mon) {
  case "jan":
    console.log("January");
    break;

  case "feb":
    console.log("February");
    break;

  case "march":
    console.log("March");
    break;

  case "april":
    console.log("April");
    break;

  default:
    break;
}


